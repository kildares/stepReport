    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        // TODO add your handling code here:
        String pass= new String(this.senhaTextField.getPassword());
        if(!this.ipTextField.equals("")&&!this.userTextField.equals("")&&!pass.equals("")){
            try {
                PrintWriter writer = new PrintWriter("config.cfg","UTF-8");
                writer.write(this.ipTextField.getText() +" " + this.userTextField.getText() + " " + pass+ " ");
                writer.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(ServerConfigPanel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(ServerConfigPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(new JFrame(), "Dados atualizados com sucesso");
        }
        else{
            JOptionPane.showMessageDialog(this, "Um ou mais campos vazios");
        }        
    }//GEN-LAST:event_confirmarButtonActionPerformed


